package org.pursuit;

public class PrimitiveConverter {

    public Integer intToInteger(int num) {
        return (Integer) num;
    }

    public Double doubleToDouble (double num) {
        return (Double) num;
    }

    public Character charToCharacter(char ch) {
        return (Character) ch;
    }

    public Boolean booleanToBoolean(boolean bool) {
        return (Boolean) bool;
    }
}
